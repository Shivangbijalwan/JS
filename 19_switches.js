// these are switch case statements
const cups = 45;
switch (cups) {
    case 4:
        console.log("value is checkedfor 4");
        break;

    case 41:
        console.log("value is checkedfor 41");
        break;
    case 43:
        console.log("value is checkedfor 43");
        break;
    case 21:
        console.log("value is checkedfor 21");
        break;
    case 1:
        console.log("value is checkedfor 1");
        break;
    default:
        console.log("the value of the cup is none of the above")
        break;
}
